// 扫描 src/components 下 examples/meta.json，生成 dev showcase 注册表与菜单
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '../..');
const DEV = resolve(ROOT, 'dev');
const SRC = resolve(ROOT, 'src');
const COMPONENTS_DIR = resolve(SRC, 'components');
const REGISTRY_PATH = resolve(DEV, 'pages', 'Components', 'examples-registry.ts');
const MENU_CONFIG_PATH = resolve(DEV, 'layouts', 'menu-config.ts');
const ROUTES_PATH = resolve(DEV, 'routes.tsx');

export function collectExamples() {
  const metaFiles = findFiles(COMPONENTS_DIR, 'meta.json');
  if (metaFiles.length === 0) {
    console.log('⚠️  未找到 examples/meta.json');
    return false;
  }

  console.log(`📦 找到 ${metaFiles.length} 个 meta.json`);

  const groups = metaFiles.map((metaFile) => {
    const meta = JSON.parse(readFileSync(metaFile, 'utf-8'));
    const examplesDir = resolve(dirname(metaFile), '..');
    const relFromComponents = relative(COMPONENTS_DIR, examplesDir);
    const route =
      '/components/' +
      relFromComponents
        .split(/[/\\]/)
        .map((s) => s.toLowerCase())
        .join('/');
    const pathParts = relFromComponents.split(/[/\\]/);
    return {
      meta,
      route,
      relFromComponents,
      dirName: pathParts[pathParts.length - 1],
      domainFolder: pathParts[0],
    };
  });

  groups.sort((a, b) => a.route.localeCompare(b.route));

  const nestedDomains = new Map();
  const flatGroups = [];

  for (const group of groups) {
    const segments = group.route.replace('/components/', '').split('/');
    if (segments.length >= 2) {
      const domainKey = segments[0];
      if (!nestedDomains.has(domainKey)) {
        nestedDomains.set(domainKey, { domainKey, folderName: group.domainFolder, groups: [] });
      }
      nestedDomains.get(domainKey).groups.push(group);
    } else {
      flatGroups.push(group);
    }
  }

  const nestedDomainList = [...nestedDomains.values()].sort((a, b) =>
    a.folderName.localeCompare(b.folderName),
  );

  let changed = false;
  if (writeIfChanged(REGISTRY_PATH, generateRegistry(groups))) changed = true;
  if (writeIfChanged(MENU_CONFIG_PATH, generateMenuConfig(nestedDomainList, flatGroups))) changed = true;
  if (writeIfChanged(ROUTES_PATH, generateRoutes(nestedDomainList, flatGroups))) changed = true;

  return changed;
}

if (process.argv[1] && fileURLToPath(import.meta.url) === resolve(process.argv[1])) {
  collectExamples();
}

function findFiles(dir, fileName) {
  const results = [];
  try {
    for (const entry of readdirSync(dir, { withFileTypes: true })) {
      const fullPath = resolve(dir, entry.name);
      if (entry.isDirectory() && entry.name !== 'node_modules') {
        results.push(...findFiles(fullPath, fileName));
      } else if (entry.name === fileName) {
        results.push(fullPath);
      }
    }
  } catch {
    /* skip */
  }
  return results;
}

function writeIfChanged(filePath, content) {
  mkdirSync(dirname(filePath), { recursive: true });
  let existing = '';
  try {
    existing = readFileSync(filePath, 'utf-8');
  } catch {
    /* first write */
  }
  if (existing === content) return false;
  writeFileSync(filePath, content, 'utf-8');
  console.log(`✅ ${relative(ROOT, filePath)}`);
  return true;
}

function generateRegistry(groups) {
  const entries = groups.map(({ meta, route }) => generateEntry(meta, route)).join(',\n');
  return `// AUTO-GENERATED by dev/scripts/collect-examples.mjs
import React from 'react';
import type { ExampleGroup } from './examples';

export const EXAMPLE_REGISTRY: Record<string, ExampleGroup> = {
${entries}
};
`;
}

function generateEntry(meta, route) {
  const examples = meta.examples.map((ex) => generateExample(ex)).join(',\n');
  let apiDoc = '';
  if (meta.apiDoc?.length) {
    const docs = meta.apiDoc.map((doc) => generateApiDoc(doc)).join(',\n');
    apiDoc = `,\n    apiDoc: [\n${docs}\n    ]`;
  }
  const desc = meta.description ? `\n    description: '${escapeStr(meta.description)}',` : '';
  return `  '${route}': {
    title: '${escapeStr(meta.title)}',${desc}
    examples: [\n${examples}\n    ]${apiDoc}
  }`;
}

function generateExample(ex) {
  const block = ex.block ? ',\n        block: true' : '';
  return `      {
        title: '${escapeStr(ex.title)}',
        description: '${escapeStr(ex.description)}',
        component: React.lazy(() => import('${ex.component}')),
        sourcePath: () => import('${ex.sourcePath}?raw')${block}
      }`;
}

function generateApiDoc(doc) {
  const rows = doc.rows
    .map((row) => {
      const parts = [
        `prop: '${escapeStr(row.prop)}'`,
        `desc: '${escapeStr(row.desc)}'`,
        `type: '${escapeStr(row.type)}'`,
      ];
      if (row.defaultVal !== undefined) parts.push(`defaultVal: '${escapeStr(row.defaultVal)}'`);
      if (row.required) parts.push('required: true');
      return `          { ${parts.join(', ')} }`;
    })
    .join(',\n');
  return `      {
        componentName: '${escapeStr(doc.componentName)}',
        rows: [\n${rows}\n        ]
      }`;
}

function generateMenuConfig(nestedDomainList, flatGroups) {
  const nestedMenus = nestedDomainList
    .map((domain) => {
      const children = domain.groups
        .map((g) => `      { key: '${g.route}', label: '${g.dirName}' },`)
        .join('\n');
      return `  {
    key: '${domain.domainKey}',
    label: '${domain.folderName}',
    children: [\n${children}\n    ],
  },`;
    })
    .join('\n');

  const flatItems = flatGroups
    .map((g) => `  { key: '${g.route}', label: '${g.dirName}' },`)
    .join('\n');

  return `// AUTO-GENERATED
import type { MenuProps } from 'antd';

export const MENU_ITEMS: MenuProps['items'] = [
${nestedMenus}
${flatItems}
];
`;
}

function generateRoutes(nestedDomainList, flatGroups) {
  const nested = nestedDomainList
    .map((domain) => {
      const subRoutes = domain.groups
        .map((g) => {
          const subPath = g.route.replace(`/components/${domain.domainKey}/`, '');
          return `      <Route path="${subPath}" element={<ExamplePage />} />`;
        })
        .join('\n');
      return `    <Route path="${domain.domainKey}">\n${subRoutes}\n    </Route>`;
    })
    .join('\n');

  const flatRoutes = flatGroups
    .map((g) => {
      const path = g.route.replace('/components/', '');
      return `    <Route path="${path}" element={<ExamplePage />} />`;
    })
    .join('\n');

  return `// AUTO-GENERATED
import ExamplePage from './pages/Components/ExamplePage';
import { Route } from 'react-router-dom';

export const showcaseRoutes = (
  <>
${nested}
${flatRoutes}
  </>
);
`;
}

function escapeStr(s) {
  return s ? s.replace(/'/g, "\\'") : '';
}
